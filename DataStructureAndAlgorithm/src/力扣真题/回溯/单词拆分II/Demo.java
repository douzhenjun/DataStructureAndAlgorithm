package 力扣真题.回溯.单词拆分II;

import java.util.ArrayList;
import java.util.List;

/**
 * @Description
 * @Author douzhenjun
 * @DATE 2024-08-20
 **/
public class Demo {
    //空集合可以遍历,不用提前判断
    public static void main(String[] args) {
        List<String> stringList = new ArrayList<>();
        for (String s : stringList) {
            System.out.println(s);
        }
        stringList.add("1");
        stringList.add("2");
        stringList.add("3");
        stringList.add("4");
        for (String s : stringList) {
            System.out.println(s);
        }
    }
}
